public class PlannigHybryde {
	private boolean valeurEtat;
	private int  jourSemaine;
	private int heureDebut;
	private int heureFin;
	private int volHoraire;
	
	
	public boolean isValeurEtat() {
		return valeurEtat;
	}

	public void setValeurEtat(boolean valeurEtat) {
		this.valeurEtat = valeurEtat;
	}


	public int getHeureDebut() {
		return heureDebut;
	}

	public void setHeureDebut(int heureDebut) {
		this.heureDebut = heureDebut;
	}

	public int getHeureFin() {
		return heureFin;
	}

	public void setHeureFin(int heureFin) {
		this.heureFin = heureFin;
	}

	public int getJourSemaine() {
		return jourSemaine;
	}

	public void setJourSemaine(int jourSemaine) {
		this.jourSemaine = jourSemaine;
	}

	
	public int getVolHoraire() {
		return volHoraire;
	}

	public void setVolHoraire(int volHoraire) {
		this.volHoraire = volHoraire;
	}

}
